    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        
        try {
            PreparedStatement ps = cn.prepareStatement("INSERT INTO empleados (nombre, apellido, direccion, telefono) VALUES (?,?,?,?)");
            
            ps.setString(1, txt_Nombre.getText() );
            ps.setString(2, txt_Apellido.getText() );
            ps.setString(3, txt_Direccion.getText() ) ;
            ps.setString(4, txt_Telefono.getText() );
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos guardados");
            
            limpiar(); 
            mostrarTabla("");
            
        } catch (SQLException e) {
            System.err.println("Error al guardar..." + e);
            JOptionPane.showMessageDialog(null, "Error al guardar");
        }
        
    }//GEN-LAST:event_btn_GuardarActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        
        int fila = this.tabla.getSelectedRow();
        
        this.txt_ID.setText(this.tabla.getValueAt(fila, 0). toString());
        this.txt_Nombre.setText(this.tabla.getValueAt(fila, 1). toString());
        this.txt_Apellido.setText(this.tabla.getValueAt(fila, 2). toString());
        this.txt_Direccion.setText(this.tabla.getValueAt(fila, 3). toString());
        this.txt_Telefono.setText(this.tabla.getValueAt(fila, 4). toString());
        
    }//GEN-LAST:event_tablaMouseClicked

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        try {
            PreparedStatement ps = cn.prepareStatement("DELETE FROM empleados WHERE id = '"+txt_ID.getText()+"'");
            
            int respuesta = ps.executeUpdate();
            if (respuesta > 0) {
                JOptionPane.showMessageDialog(null, "Empleado eliminado");
                limpiar();
                mostrarTabla(""); 
            } else {
                JOptionPane.showMessageDialog(null, "No ha seleccionado fila");
            }
        } catch (SQLException e) {
            System.err.println("Erorr al eliminar..." + e);
            JOptionPane.showMessageDialog(null,"Error al eliminar"); 
            
        }
    }//GEN-LAST:event_eliminarActionPerformed

