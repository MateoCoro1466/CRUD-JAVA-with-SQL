    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        
        try {
            PreparedStatement ps = cn.prepareStatement("UPDATE empleados SET nombre = '"+txt_Nombre.getText()+"', apellido = '"+txt_Apellido.getText()+"', direccion = '"+txt_Direccion.getText()+"', telefono = '"+txt_Telefono.getText()+"' WHERE id = '"+txt_ID.getText()+"'"); 
            int respuesta = ps.executeUpdate(); 
            
            if (respuesta > 0) {
                JOptionPane.showMessageDialog(null, "Datos actualizados"); 
                limpiar();
                mostrarTabla("");
            } else {
                JOptionPane.showMessageDialog(null, "No hay seleccionado fila"); 
            }
            
            
            
        } catch (Exception e) {
            System.err.println("Erorr al actualizar..." + e);
            JOptionPane.showMessageDialog(null, "Error al actualizar");
        }
        
    }//GEN-LAST:event_btn_ActualizarActionPerformed

